package July01.Operators;

public class arithmeticOper {
    public static void main (String[] args) 
    {
          
        // Arithmetic operators on integers
        int a = 10;
        int b = 3;
           
        System.out.println("a + b = " + (a + b));
        System.out.println("a - b = " + (a - b));
        System.out.println("a * b = " + (a * b));
        System.out.println("a / b = " + (a / b));
        System.out.println("a % b = " + (a % b));
          
    }
}
